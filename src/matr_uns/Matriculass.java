
package matr_uns;
import java.util.Date;

public class Matriculass {
    private String idMatriculass;
    private Date fechaMatriculass;
    private String estado;
    private String periodoAcademico;
    private String idPago;
    private String idUsers;
    
    public Matriculass(){}

    public String getIdMatriculass() {
        return idMatriculass;
    }

    public void setIdMatriculass(String idMatriculass) {
        this.idMatriculass = idMatriculass;
    }

    public Date getFechaMatriculass() {
        return fechaMatriculass;
    }

    public void setFechaMatriculass(Date fechaMatriculass) {
        this.fechaMatriculass = fechaMatriculass;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getPeriodoAcademico() {
        return periodoAcademico;
    }

    public void setPeriodoAcademico(String periodoAcademico) {
        this.periodoAcademico = periodoAcademico;
    }

    public String getIdPago() {
        return idPago;
    }

    public void setIdPago(String idPago) {
        this.idPago = idPago;
    }

    public String getIdUsers() {
        return idUsers;
    }

    public void setIdUsers(String idUsers) {
        this.idUsers = idUsers;
    }
    
    
    
}
