    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      Connection conn = null;
    CallableStatement cs = null;
    
    try {
        // Conectar a la base de datos
        DB_connection db = new DB_connection();
        conn = db.conectar();

        // Llamar al procedimiento almacenado
        String sql = "{CALL BuscarEstudiante(?, ?, ?, ?, ?, ?)}";
        cs = conn.prepareCall(sql);

        // Pasar el CUI desde el campo de b√∫squeda
        cs.setString(1, CUY.getText());

        // Definir los par√°metros de salida
        cs.registerOutParameter(2, java.sql.Types.VARCHAR);
        cs.registerOutParameter(3, java.sql.Types.VARCHAR);
        cs.registerOutParameter(4, java.sql.Types.VARCHAR);
        cs.registerOutParameter(5, java.sql.Types.VARCHAR);
        cs.registerOutParameter(6, java.sql.Types.VARCHAR);

        // Ejecutar el procedimiento
        cs.execute();

        // Obtener los resultados
        String dni = cs.getString(2);
        String nombre = cs.getString(3);
        String apPaterno = cs.getString(4);
        String apMaterno = cs.getString(5);
        String correo = cs.getString(6);

        // Mostrar los datos si existen
        if (dni != null) {
            CUY2.setText(dni);
            CUY1.setText(nombre);
            CUY5.setText(apPaterno);
            CUY3.setText(apMaterno);
            CUY4.setText(correo);
        } else {
            JOptionPane.showMessageDialog(this, "No se encontr√≥ el estudiante.", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
        }

    } catch (SQLException e) {
        System.err.println(e);
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } finally {
        try {
            if (cs != null) cs.close();
            if (conn != null) conn.close();
        } catch (SQLException e) {
            System.err.println(e);
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

